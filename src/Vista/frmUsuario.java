    private void tableUsuarioMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tableUsuarioMouseClicked
        try {
            int fila = tableUsuario.getSelectedRow();
            txtidUsuario.setText(tableUsuario.getValueAt(fila, 0).toString());
            txtNombre.setText(tableUsuario.getValueAt(fila, 1).toString());
            txtApellidoUsuario1.setText(tableUsuario.getValueAt(fila, 2).toString());
            txtApellidoUsuario2.setText(tableUsuario.getValueAt(fila, 3).toString());
            txtEmailUsuario.setText(tableUsuario.getValueAt(fila, 4).toString());
            txtNombreUsuario.setText(tableUsuario.getValueAt(fila, 5).toString());
            cmbTipoUsuario.setSelectedItem(tableUsuario.getValueAt(fila, 6));
            txtPassword.setText(tableUsuario.getValueAt(fila, 7).toString());
            cbxActivo.setSelected(Boolean.parseBoolean(tableUsuario.getValueAt(fila, 8).toString()));

        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Error al seleccionar un registro: " + e.getMessage());
        }
    }//GEN-LAST:event_tableUsuarioMouseClicked

